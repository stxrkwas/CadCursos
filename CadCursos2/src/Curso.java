    private void editMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editMouseClicked
        
        //Criando strings para armazenar os dados digitados nos campos de texto
        String nome = txtNC.getText();
        String qt = txtQ.getText();
        String di = txtDI.getText();
        String dt = txtDT.getText();
        String ch = txtCH.getText();
        String p = txtP.getText();

        //
        String sql = "";
        
        //
        String msg = "";
        
        try{
            
            if(txtCod.getText().equals("")){
                
                sql = "insert into curso (NomeCurso, QuantTurmas, DataInicio, DataTermino, CargaHoraria, Preco) values (" + nome 
                        + "', '" + qt + "', '" + di + "', '" + dt + "', '" +  ch + "', '" + p + "' where IdCurso = " + txtCod.getText();
                
                msg = "Gravação de um novo registro";
            
            }
            
            else{
                
                sql = "update curso set NomeCurso = '" + nome + "', QuantTurmas = '" + qt + "', DataInicio = '" + di + "', DataTermino = '" + dt
                        + "', CargaHoraria = '" + ch + "', Preco = '" + p + "' where IdCurso = " + txtCod.getText();
                
                msg = "Alteração de registro ";
            
            }
            
            con_cliente.statement.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, msg + "realizada com sucesso!", "Mensagem do programa ", JOptionPane.INFORMATION_MESSAGE);
            
            con_cliente.executaSQL("select * from curso order by IdCurso");
            con_cliente.resultset.first();
            preencherTabela();
            mostrar_Dados();
        }
        
        catch(SQLException errosql){
            
            JOptionPane.showMessageDialog(null, "Erro de gravação :\n " + errosql, "Mensagem do programa ", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_editMouseClicked

