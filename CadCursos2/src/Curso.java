    public void preencherTabela(){
        
        curso.getColumnModel().getColumn(0).setPreferredWidth(70);
        curso.getColumnModel().getColumn(1).setPreferredWidth(70);
        curso.getColumnModel().getColumn(2).setPreferredWidth(70);
        curso.getColumnModel().getColumn(3).setPreferredWidth(70);
        curso.getColumnModel().getColumn(4).setPreferredWidth(70);
        curso.getColumnModel().getColumn(5).setPreferredWidth(70);
        curso.getColumnModel().getColumn(6).setPreferredWidth(70);
        
        DefaultTableModel modelo = (DefaultTableModel) curso.getModel();
        modelo.setNumRows(0);
        
        try{
            
            con_cliente.resultset.beforeFirst();
            
            while(con_cliente.resultset.next()){
                
                modelo.addRow(new Object[]{
                    
                    con_cliente.resultset.getString("IdCurso"), con_cliente.resultset.getString("NomeCurso"), con_cliente.resultset.getString("QuantTurmas"),
                    con_cliente.resultset.getString("DataInicio"), con_cliente.resultset.getString("DataTermino"), con_cliente.resultset.getString("CargaHoraria"), 
                    con_cliente.resultset.getString("Preco")
                        
                });
                       
            }
              
        }
          
        catch(SQLException erro){
                
            JOptionPane.showMessageDialog(null, "\n Erro ao listar dados da tabela!! :\n" + erro, "Mensagem do programa ", JOptionPane.INFORMATION_MESSAGE);    
                
        }   
            
    }
    
    public void posicionarRegistro(){
        
        try{
            
            con_cliente.resultset.first();
            mostrar_Dados();
            
        }
        
        catch(SQLException erro){
            
            JOptionPane.showMessageDialog(null, "Não foi possível posicionar no primeiro registro: " + erro, "Mensagem do programa ", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }
    
    public void mostrar_Dados(){
        
        try{
            
            txtCod.setText(con_cliente.resultset.getString("IdCurso"));
            txtNC.setText(con_cliente.resultset.getString("NomeCurso"));
            txtQ.setText(con_cliente.resultset.getString("QuantTurmas"));
            txtDI.setText(con_cliente.resultset.getString("DataInicio"));
            txtDT.setText(con_cliente.resultset.getString("DataTermino"));
            txtCH.setText(con_cliente.resultset.getString("CargaHoraria"));
            txtP.setText(con_cliente.resultset.getString("Preco"));
            
            
        }
        
        catch(SQLException erro){
            
            JOptionPane.showMessageDialog(null, "Não localizou dados: "+erro, "Mensagem do programa", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
